const CACHE_NAME = 'arcwiki-v1'; // Change this to a unique name for your app
const urlsToCache = [
  '/',
  'index.html',
  'manifest.json',
  'icon512_maskable.png', // Add other static assets as needed
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => cache.addAll(urlsToCache))
  );
});

self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request)
      .then((response) => response || fetch(event.request))
  );
});
